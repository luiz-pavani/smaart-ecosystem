-- Supabase table schema for user_fed_lrsj (CSV import)
CREATE TABLE public.user_fed_lrsj (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  member_no INTEGER,
  graduacao VARCHAR(50),
  dan VARCHAR(20),
  name VARCHAR(255),
  email VARCHAR(100),
  phone VARCHAR(30),
  city VARCHAR(50),
  province VARCHAR(10),
  birthdate DATE,
  age INTEGER,
  gender VARCHAR(20),
  member_since TIMESTAMP,
  nationality VARCHAR(50),
  residence VARCHAR(50),
  plan VARCHAR(100),
  plan_status VARCHAR(30),
  expire_date DATE,
  member_status VARCHAR(30),
  academies VARCHAR(100),
  identidade_img TEXT,
  patch_size VARCHAR(30),
  patch_name VARCHAR(100),
  foto TEXT,
  nivel_arbitragem VARCHAR(50),
  certificado_dan TEXT,
  observacoes TEXT,
  lote VARCHAR(20)
);
